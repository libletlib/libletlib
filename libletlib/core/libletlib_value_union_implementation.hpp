#ifndef LIBLETLIB_LIBLETLIB_VALUE_UNION_IMPLEMENTATION_HPP
#define LIBLETLIB_LIBLETLIB_VALUE_UNION_IMPLEMENTATION_HPP

namespace libletlib
{
	namespace detail
	{
#if (__cplusplus >= 201103L)
	#ifndef LIBLETLIB_FREESTANDING
		/// \brief Var union constructor for arrays.
		///
		/// \param _list from list initialization.
		value_union::value_union(std::initializer_list<var> _list) noexcept
		{
			var* array        = allocate<var>(_list.size());
			std::size_t index = 0;
			for (var const& element : _list)
			{
				array[index] = element;
				++index;
			}
			array_type = array;
		}
	#endif
#endif
	}// namespace detail
}// namespace libletlib

#endif//LIBLETLIB_LIBLETLIB_VALUE_UNION_IMPLEMENTATION_HPP
